public class IfElseExample2 {
    public static void main(String[] args) {
      // Declare a boolean variable and set it to true
      boolean isRaining = true;

      // Use an if-else statement to check if it's raining
      if (isRaining) {
          System.out.println("It's raining. Bring an umbrella.");
      } else {
          System.out.println("It's not raining. You don't need an umbrella.");
      }
   }
}
