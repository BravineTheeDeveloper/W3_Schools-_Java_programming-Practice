//The program below demonstrates the use of a short hand if else in Java
//It's also known as "ternary operator" because it consists of three operands.

public class ShortHandIsElse {
public static void main(String[] args){
    int age = 18;
    String result = (age >= 18) ? "You are eligible!" : "You are not eligible";
    System.out.println(result);
}
}
